def print_lev(tiles, hero_x, hero_y, minimap = True, borders = True):
    # put tiles on sectors
    id = 1
    n_tile_x = len(tiles[0])
    n_tile_y = len(tiles)

    n_sectors_x = (n_tile_x * 32) // SECTOR_W
    n_sectors_y = (n_tile_y * 32) // SECTOR_H
    
    hero_y = 50 - hero_y
    tiles[hero_y][hero_x] = 0.0

    hero_sec_x = (hero_x * 32) // SECTOR_W
    hero_sec_y = ((50 * 32) - (hero_y * 32)) // SECTOR_H

    level = dict()

    for sec_y in range(n_sectors_y):
        level[sec_y] = {sec_x: [] for sec_x in range(n_sectors_x)}

    old_y = 20

    for y in range(n_tile_y):
        row = ""
        old_x = 0

        y_pos = (n_tile_y*32) - y*32
        tile_sec_y = y_pos//SECTOR_H

        if tile_sec_y < old_y and borders:
            print("-" * (n_tile_x+10))
            old_y = tile_sec_y

        for x in range(n_tile_x):

            tile = map2[y][x]

            x_pos = x*32
            tile_sec_x = x_pos//SECTOR_W
            
            if tile_sec_x > old_x and borders:
                row+="|"
                old_x = tile_sec_x

            if tile != -1:
                val = str(tile).split(".")

                if tile == 0.0:
                    row+="X"
                else:
                    row+="O"
                
                
                # print(f"tile pos {x} {y}")
                # print(f"for sec {tile_sec_x} {tile_sec_y}")
                level[tile_sec_y][tile_sec_x].append((int(val[1]), int(val[0]), x_pos, y_pos))
                
                assert (tile_sec_x*SECTOR_W <= x_pos)
                assert (x_pos <= tile_sec_x*SECTOR_W + SECTOR_W)
                
                y_min = tile_sec_y*SECTOR_H
                y_max = tile_sec_y*SECTOR_H + SECTOR_H

                # print(f"asserting {y_min} <= {y_pos} <= {y_max}\n")
                assert (y_min <= y_pos)
                assert (y_pos <= y_max)
            else:
                row+="."

        if minimap:
            print(row)

    if minimap:
        return

